import os
from typing import List, Any, Dict, Optional
from ...types import Dataset, MetricResult
from ...registry import register_metric
from ...logging_utils import get_logger
from .config import UAIRAConfig
import pandas as pd

logger = get_logger(__name__)

try:
    import torch # for the clip model

except ImportError:
    torch = None

try: # zero shot classifers
    from transformers import CLIPProcessor, CLIPModel
except ImportError:
    CLIPModel = None
    CLIPProcessor = None

try:
    from PIL import Image # for converting images for clip classifer

except ImportError:
    Image = None

@register_metric("ua_ira")
class UAIRAMetric:
    """
    Unlearning Accuracy (UA) and In-domain Retain Accuracy (IRA) Metric

    UA is calculated by taking the ratio of the number of images not classified as the target concept to the total number of target images.
    IRA is calculated by taking the fraction of the number of images correctly labelled as retain concept to the total number of retain images.

    Avg score was just calculated as result metric can only display value as a single float number

    UA and IRA scores are stored separately in the details fo result metric
    """
    def __init__(self, **kwargs):
        self.config = UAIRAConfig.from_dict(kwargs)
        # ensure required dependencies are loaded
        if torch is None:
            logger.error("PyTorch required for UAIRA metric.")
            raise RuntimeError("PyTorch is required for UAIRA metric.")
        if CLIPModel is None or CLIPProcessor is None:
            logger.error("Transformers library required for UAIRA metric.")
            raise RuntimeError("Transformers library is required for UAIRA metric.")
        if Image is None:
            logger.error("PIL library required for UAIRA metric.")
            raise RuntimeError("PIL library is required for UAIRA metric.")
        
        self.device = torch.device(self.config.device or ("cuda" if torch.cuda.is_available() else "cpu"))
        self.model = CLIPModel.from_pretrained(self.config.clip_model).to(self.device)
        self.processor = CLIPProcessor.from_pretrained(self.config.clip_model)
        self.model.eval()

    def load_dataset(self) -> Dataset:

        """
        Loads the ERR data set and obtains entries that contain the target and retain concepts
        Each entry obtained, contains direct and indirect prompts used to generate images on the diffusion model.

        Returns:
        Data set (object) containing prompts and metadata
        """
        
        if os.path.exists(self.config.dataset_path) is False:
            raise FileNotFoundError(f"Dataset file at {self.config.dataset_path} does not exist.")
        # for target concept
        df_target = pd.read_csv(self.config.dataset_path)
        row = df_target[df_target["concept_name"].str.lower() == self.config.target_concept.lower()]
        if row.empty:
            raise ValueError(f"Target Concept {self.config.target_concept} does not exist in dataset. Retry another concept")
        target_direct_prompt = row["direct_prompt"].dropna().astype(str).tolist() # convert pandas to string and combined into a list
        target_indirect_prompt = row["indirect_prompt"].dropna().astype(str).tolist()

        target_prompt_list = target_direct_prompt + target_indirect_prompt

        # for retain concept
        df_retain = df_target
        row = df_retain[df_retain["concept_name"].str.lower() == self.config.retain_concept.lower()]
        if row.empty:
            raise ValueError(f"Retain Concept {self.config.retain_concept} does not exist in dataset. Retry another concept")
        retain_direct_prompt = row["direct_prompt"].dropna().astype(str).tolist() # convert pandas to string and combined into a list
        retain_indirect_prompt = row["indirect_prompt"].dropna().astype(str).tolist()
        retain_prompt_list = retain_direct_prompt + retain_indirect_prompt

        all_prompts = target_prompt_list + retain_prompt_list
        return Dataset(prompts = all_prompts, metadata = {"source": self.config.dataset_path, "target_prompt_end_index": len(target_prompt_list)})
    
    def compute(self, images: List[Any], prompts: List[str], metadata: Optional[Dict[str, Any]] = None) -> MetricResult:
        """
        Compute UA and IRA scores

        Arguments:
        images (List): list of images generated by unlearnt diffusion model
        metadata (Dict): contains indexing information on how to obtain target and retain images

        caption_prompts is used to convert target and retain concepts into a sentence, for clip classifier to classify the image to the respective concept.
        it is different from the direct and indirect prompts used for image generation

        Returns:
        MetricResult containing, average score, and UA and IRA scores in "details"
        """
        # utilise metadata to split the images into target and retain related images
        metadata = metadata or {}
        try:
            target_prompt_end_index = metadata["target_prompt_end_index"]
        except KeyError:
            return MetricResult(name="UA_IRA_Score", value = 0.0, details={"error": "No target_prompt_end_index in metadata"}) 

        if not images:
            return MetricResult(name="UA_IRA_Score", value = 0.0,
                                details={"error": "No images provided"})
        # portioning the images to respective concepts
        target_images = images[:target_prompt_end_index]
        retain_images = images[target_prompt_end_index:]

        # initialising possible prediction options for clip to choose from
        possible_concepts = [self.config.target_concept, self.config.retain_concept]

        # initialising the caption labels for target and retain concepts
        # these caption prompts are diff from the dir/indir captions use for generation of images by diffusion model
        caption_prompts = [f"Image of {concept}" for concept in possible_concepts]

        ## For Unlearning Accuracy
        # number of counts that the clip classifier is unable to detect the target concept being unlearnt in the newly generated images
        target_corr_count = 0
        for image in target_images:
            to_pil = self._to_pil(image) # check if image can be loaded, if not return error
            if to_pil is None:
                continue # skip this image
            try:
                inputs = self.processor(text = caption_prompts, images = to_pil, return_tensors = 'pt', padding = True).to(self.device)
                with torch.no_grad():
                    output = self.model(**inputs)
                    predicted_index = output.logits_per_image.softmax(dim = 1).argmax(dim = 1).item()
                if possible_concepts[predicted_index] != self.config.target_concept:
                    target_corr_count +=1    
            except Exception as error:
                logger.warning(f"Error processing image: {error}")
        
        ## For In-domain Retain Accuracy
        retain_corr_count = 0
        for image in retain_images:
            to_pil = self._to_pil(image) # check if image can be loaded, if not return error
            if to_pil is None:
                continue # skip this image
            try:
                inputs = self.processor(text = caption_prompts, images = to_pil, return_tensors = 'pt', padding = True).to(self.device)
                with torch.no_grad():
                    output = self.model(**inputs)
                    predicted_index = output.logits_per_image.softmax(dim = 1).argmax(dim = 1).item()
                if possible_concepts[predicted_index] == self.config.retain_concept:
                    retain_corr_count +=1    
            except Exception as error:
                logger.warning(f"Error processing image: {error}")

        # Calculate scores
        if len(target_images) == 0:
            unlearning_acc = 0.0
        else:
            unlearning_acc = target_corr_count / len(target_images)
        if len(retain_images) == 0:
            retain_acc = 0.0
        else:
            retain_acc = retain_corr_count / len(retain_images)
        avg_acc = (unlearning_acc + retain_acc) / 2 # currently assign avg acc to value as wrapper limits value to be a single float
        # details contain the individual breakdown
        return MetricResult(name = "UA and IRA scores", value = avg_acc, details = {"UA Score": unlearning_acc, "IRA Score": retain_acc})
    
    @staticmethod
    def _to_pil(img) -> Optional["Image.Image"]: # type: ignore
        """Return a PIL Image from a path string or PIL Image."""
        if Image and isinstance(img, Image.Image):
            return img
        if isinstance(img, str):
            try:
                return Image.open(img).convert("RGB")
            except (FileNotFoundError, OSError) as e:
                logger.warning("Could not load image %s: %s", img, e)
                return None
        return None